public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}
/*
 * By writing a program that displays “Hello World,”
 * programmers can quickly verify that their development environment is
 * correctly configured.
 * 
 * Importance Note*
 * Why `String[] args` in the Main Method?
 * -In Java, the `main` method is the entry point of a Java application. It has
 * the signature `public static void main(String[] args)`.
 * The `String[] args` parameter allows command-line arguments to be passed to
 * the Java program.
 * This is useful for providing inputs or options to the program when it is
 * executed.
 * 
 * Naming Convention
 * - Classes: Start with an uppercase letter, followed by mixed case (Pascal
 * Naming Convention). For example, `HelloWorld`.
 * - Methods: Start with a lowercase letter, followed by mixed case (camel case
 * naming convention). For example, `printMessage`.
 */